package AdditionalTasks;

public class Task14 {
    public void multiplicationTable() {
        for (int i = 1; i <= 9; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.println(i + " * " + j + " = " + (i * j));
            }
            System.out.println();
        }

    }
}
